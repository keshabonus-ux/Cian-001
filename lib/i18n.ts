export function getText(lang: Lang, key: TKey): string {
  const d = DICT[lang];
  return (d as Record<string, string>)[key] ?? DICT.ru[key] ?? key;
}

export function pluralListings(lang: Lang, count: number): string {
  if (lang === "ru") {
    const mod10 = count % 10;
    const mod100 = count % 100;
    if (mod10 === 1 && mod100 !== 11) return DICT.ru["home.count.listings_1"];
    if ([2, 3, 4].includes(mod10) && ![12, 13, 14].includes(mod100))
      return DICT.ru["home.count.listings_2"];
    return DICT.ru["home.count.listings_5"];
  }
  return getText(lang, count === 1 ? "home.count.listings_1" : "home.count.listings_5");
}
